package miniJava.CodeGen;

 

public abstract class RuntimeEntity { 
	
	public int size; 
	public int address;
	
	 public RuntimeEntity ( ) {
		   size=0;
		   address=0;
		   		   
		  }
	 
	 public RuntimeEntity (int s,int add ) {
		   size=s;
		   address=add;
		   		   
		  }
	
	
}



